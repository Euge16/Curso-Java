public class Constantes {
    public static void main(String[] args) {
        System.out.println("*** Constantes en Java ***");
        final var DIAS_SEMANA = 7;
        System.out.println("DIAS_SEMANA = " + DIAS_SEMANA);

        final var PI = 3.1415;
        System.out.println("PI = " + PI);
        System.out.println("Math.PI "+ Math.PI);
        final var MENSAJE_BIENVENIDA = "Bienvenida a la Universidad Java";
        System.out.println("MENSAJE_BIENVENIDA = " + MENSAJE_BIENVENIDA);
        final var MINUTO_POR_SEGUNDO = 60;
        System.out.println("MINUTO_POR_SEGUNDO = " + MINUTO_POR_SEGUNDO);
    }
}
