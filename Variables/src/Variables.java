public class Variables {
    public static void main(String[] args) {
        int alturaCm = 154;
        double alturaMetro = 1.54;
        boolean esVerdad = true;
        char genero;
        genero = 'F'; // F = Femenino    M = Masculino

        //Acceder a las variables
        System.out.println(alturaCm);

        //Modificar la variable
        alturaCm=155;
        System.out.println(alturaCm);

        //Imprimir el resto de las variables
        System.out.println(alturaMetro);
        System.out.println(esVerdad);
        System.out.println(genero);
    }
}
